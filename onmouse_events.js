{
    document.getElementById("msg").innerHTML="Mouse is placed over div";
}

function mout()
{
    document.getElementById("msg").innerHTML="Mouse is moved away div";
}

function moveup()
{
    document.getElementById("msg").innerHTML="Mouse button pressed and released";
}

function movedown()
{
    document.getElementById("msg").innerHTML="left Mouse button pressed down";
}